    private void btnFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindActionPerformed
            
        String id = JOptionPane.showInputDialog(this, "Nhập mã khách hàng cần tìm:");
        if (id == null || id.trim().isEmpty()) return;

        KhachHangDAO dao = new KhachHangDAO();
        KhachHang kh = dao.findById(id.trim());

        if (kh == null) {
            JOptionPane.showMessageDialog(this, "Không tìm thấy khách hàng!");
            return;
        }

        // Đưa dữ liệu lên TextField
        txtMKH.setText(kh.getMaKH());
        txtHo.setText(kh.getHo());
        txtTenLot.setText(kh.getTenLot());
        txtTen.setText(kh.getTen());
        txtCCCD.setText(kh.getCCCD());
        txtSDT.setText(kh.getSDT());
        txtNamSinh.setText(String.valueOf(kh.getNamSinh()));
        txtDiaChi.setText(kh.getDiaChi());

        if (kh.getGioiTinh().equalsIgnoreCase("Nam")) {
            rdNam.setSelected(true);
        } else {
            rdNu.setSelected(true);
        }

        JOptionPane.showMessageDialog(this, "Đã load thông tin khách hàng!");
        
    }//GEN-LAST:event_btnFindActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        
        KhachHangDAO KHD = new KhachHangDAO();
        KhachHang KH = new KhachHang();
        
        String maKH = txtMKH.getText().trim();
        String ho = txtHo.getText().trim();
        String tenLot = txtTenLot.getText().trim();
        String ten = txtTen.getText().trim();
        String cccd = txtCCCD.getText().trim();
        String sdt = txtSDT.getText().trim();
        int namSinh = Integer.parseInt(txtNamSinh.getText().trim());
        String diaChi = txtDiaChi.getText().trim();
        String gioiTinh = rdNam.isSelected() ? "Nam" : "Nữ";
        
        KH.setMaKH(maKH);
        KH.setCCCD(cccd);
        KH.setHo(ho);
        KH.setTenLot(tenLot);
        KH.setTen(ten);
        KH.setSDT(sdt);
        KH.setNamSinh(namSinh);
        KH.setDiaChi(diaChi);
        KH.setGioiTinh(gioiTinh);

        DefaultTableModel model = (DefaultTableModel) tbShowTTKH.getModel();
        model.addRow(new Object[] {
            maKH, ho, tenLot, ten, cccd, sdt, namSinh, diaChi, gioiTinh
        });
        
        KHD.insert(KH);
        
        JOptionPane.showMessageDialog(this, "Đã thêm thành công!");
        
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        
        int row = tbShowTTKH.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Hãy chọn dòng cần xóa trên bảng!");
            return;
        }
        
        String ID = tbShowTTKH.getValueAt(row, 0).toString();
        
        int confirm = JOptionPane.showConfirmDialog(this, 
                "Bạn có chắc muốn xóa dòng này?", 
                "Xác nhận", JOptionPane.YES_NO_OPTION);

        if (confirm != JOptionPane.YES_OPTION) return;

        DefaultTableModel model = (DefaultTableModel) tbShowTTKH.getModel();
        model.removeRow(row);
        
        KhachHangDAO KHD = new KhachHangDAO();
        KHD.delete(ID);
        
        JOptionPane.showMessageDialog(this, "Đã xóa thành công!");
    }//GEN-LAST:event_btnDeleteActionPerformed

