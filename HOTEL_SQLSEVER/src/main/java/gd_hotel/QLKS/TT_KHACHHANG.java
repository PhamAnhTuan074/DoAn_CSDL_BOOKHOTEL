    public void TransTableToTextField(){
        int row = tbShowTTKH.getSelectedRow();
        if (row != -1) {
            String ID = tbShowTTKH.getValueAt(row, 0).toString();
            KhachHangDAO dao = new KhachHangDAO();
            KhachHang kh = dao.findById(ID.trim());
            txtMKH.setText(kh.getMaKH());
            txtHo.setText(kh.getHo());
            txtTenLot.setText(kh.getTenLot());
            txtTen.setText(kh.getTen());
            txtCCCD.setText(kh.getCCCD());
            txtSDT.setText(kh.getSDT());
            txtNamSinh.setText(String.valueOf(kh.getNamSinh()));
            txtDiaChi.setText(kh.getDiaChi());

            if (kh.getGioiTinh().equalsIgnoreCase("Nam")) {
                rdNam.setSelected(true);
                rdNu.setSelected(false);
            } else {
                rdNu.setSelected(true);
                rdNam.setSelected(false);
            }
        }
    }
    
    
